# *** Циклы For and While


num = range(5)
str_num = ''

# Цикл For
for i in num:
    str_num = str_num + str(i) 
    print("Строка:", str_num)
        

max_num  = 5
str_num = ''
i = 0

# Цикл While
while max_num > i:
    str_num = str_num + str(i) 
    print("Строка:", str_num)
    i += 1
    

# End